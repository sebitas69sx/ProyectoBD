    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jTable1.getSelectedRow() >= 0) {
            int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            String nombre = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
            String dir = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
            String tel = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
            
            Local loc = new Local(id, nombre, dir, tel);
            frmEditLocal frm = new frmEditLocal("Modificar");
            frm.setDatos(loc);
            frm.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "SELECCIONE LA FILA A MODIFICAR");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       if (jTable1.getSelectedRow() >= 0) {
            LocalDAO libDAO = new LocalDAO();
            int resp = JOptionPane.showConfirmDialog(null, "Eliminar Libro", "Â¿Seguro que desea eliminar el libro de la Base de Datos?", JOptionPane.YES_NO_OPTION);
            if (resp == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                String nombre = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
                String dir = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
            String tel = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
                

                 Local loc = new Local(id, nombre, dir, tel);
                libDAO.EliminarLocal(loc);
                ListarLocales();
            }
        } else {
            JOptionPane.showMessageDialog(null, "SELECCIONE LA FILA A ELIMINAR");
        }
       cant();
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String busqueda = JOptionPane.showInputDialog(this,
            "Ingrese el nombre, direccion o telefono a buscar:",
            "Buscar Local",
            JOptionPane.QUESTION_MESSAGE);

        if (busqueda != null && !busqueda.trim().isEmpty()) {
            LocalDAO libroDAO = new LocalDAO();
            libroDAO.buscarLocal(busqueda);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

