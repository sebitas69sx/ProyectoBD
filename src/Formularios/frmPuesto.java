    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     if (jTable1.getSelectedRow() >= 0) {
        int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        String nombre = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String descripcion = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();

        Puesto puesto = new Puesto(id, nombre, descripcion);
        frmEditPuesto frm = new frmEditPuesto("Modificar");
        frm.setDatos(puesto);
        frm.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(null, "SELECCIONE LA FILA A MODIFICAR");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    if (jTable1.getSelectedRow() >= 0) {
        PuestoDAO puestoDAO = new PuestoDAO();
        int resp = JOptionPane.showConfirmDialog(null, "Eliminar Puesto", "Â¿Seguro que desea eliminar el puesto de la Base de Datos?", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            String nombre = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
            String descripcion = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();

            Puesto puesto = new Puesto(id, nombre, descripcion);
            puestoDAO.EliminarPuesto(puesto);
            ListarPuestos();
        }
    } else {
        JOptionPane.showMessageDialog(null, "SELECCIONE LA FILA A ELIMINAR");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

