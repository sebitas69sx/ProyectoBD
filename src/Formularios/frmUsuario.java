    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
   if (jTable1.getSelectedRow() >= 0) {
            int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            String cedula = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
            String nombre = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
            String apellido = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
            String telefono = jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString();
            String direccion = jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString();
            
            Usuario user = new Usuario(cedula, nombre, apellido, telefono, direccion, id);
            frmEditUsuario frm = new frmEditUsuario("Modificar");
            frm.setDatos(user);
            frm.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione la fila a modificar");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    if (jTable1.getSelectedRow() >= 0) {
            UsuarioDAO userDAO = new UsuarioDAO();
            int resp = JOptionPane.showConfirmDialog(null, "Eliminar Usuario", "Â¿Seguro que desea eliminar el usuario de la Base de Datos?", JOptionPane.YES_NO_OPTION);
            if (resp == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                String cedula = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
                String nombre = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
                String apellido = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
                String telefono = jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString();
                String direccion = jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString();
                
                Usuario user = new Usuario(cedula, nombre, apellido, telefono, direccion, id);
                userDAO.EliminarUsuario(user);
                ListarUsuarios();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione la fila a eliminar");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

